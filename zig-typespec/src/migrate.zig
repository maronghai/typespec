const std = @import("std");

pub fn generateMigration(
    alloc: std.mem.Allocator,
    old_sql: []const u8,
    new_sql: []const u8,
) ![]const u8 {
    // Parse both SQL outputs into table blocks
    const old_tables = try parseCreateTables(alloc, old_sql);
    const new_tables = try parseCreateTables(alloc, new_sql);

    var aw = std.Io.Writer.Allocating.init(alloc);
    const w = &aw.writer;

    try w.writeAll("-- Migration: schema diff\n");
    try w.writeAll("-- Generated by zig-typespec migrate\n\n");
    try w.writeAll("BEGIN;\n\n");

    // Build name→table maps
    var old_map = std.StringHashMap(usize).init(alloc);
    for (old_tables, 0..) |t, i| try old_map.put(t.name, i);
    var new_map = std.StringHashMap(usize).init(alloc);
    for (new_tables, 0..) |t, i| try new_map.put(t.name, i);

    // Detect quoting style from first table
    const quote_char: u8 = if (old_tables.len > 0 and old_tables[0].body.len > 15 and old_tables[0].body[14] == '"') '"' else '`';

    // DROP tables in old but not new
    for (old_tables) |ot| {
        if (!new_map.contains(ot.name)) {
            try w.print("DROP TABLE IF EXISTS {c}{s}{c};\n\n", .{ quote_char, ot.name, quote_char });
        }
    }

    // CREATE tables in new but not old
    for (new_tables) |nt| {
        if (!old_map.contains(nt.name)) {
            try w.writeAll(nt.body);
            try w.writeAll("\n\n");
        }
    }

    // ALTER tables in both — compare body text
    for (old_tables) |ot| {
        if (new_map.get(ot.name)) |ni| {
            const nt = new_tables[ni];
            if (!std.mem.eql(u8, ot.body, nt.body)) {
                // Tables differ — emit DROP + CREATE as migration
                try w.print("-- ALTER TABLE {c}{s}{c} (drop and recreate)\n", .{ quote_char, ot.name, quote_char });
                try w.print("DROP TABLE IF EXISTS {c}{s}{c};\n\n", .{ quote_char, ot.name, quote_char });
                try w.writeAll(nt.body);
                try w.writeAll("\n\n");
            }
        }
    }

    try w.writeAll("COMMIT;\n");

    try w.flush();
    var out = aw.toArrayList();
    return try out.toOwnedSlice(alloc);
}

const CreateTable = struct {
    name: []const u8,
    body: []const u8, // Full CREATE TABLE ... ; statement
};

fn parseCreateTables(alloc: std.mem.Allocator, sql: []const u8) ![]const CreateTable {
    var tables = try std.ArrayList(CreateTable).initCapacity(alloc, 16);

    var i: usize = 0;
    while (i < sql.len) {
        // Find next CREATE TABLE — handle both backtick (MySQL) and double-quote (PG) quoting
        const ct_mysql = findSubstring(sql, i, "CREATE TABLE `");
        const ct_pg = findSubstring(sql, i, "CREATE TABLE \"");
        const ct_pos: usize = if (ct_mysql) |m|
            if (ct_pg) |p| @min(m, p) else m
        else
            ct_pg orelse break;

        const is_pg = sql[ct_pos + 14] == '"';
        const close_q: u8 = if (is_pg) '"' else '`';

        const name_start = ct_pos + 14; // len("CREATE TABLE `") or len("CREATE TABLE \"")
        if (findChar(sql, name_start, close_q)) |name_end| {
            const name = sql[name_start..name_end];
            // Find the end of the statement (closing `;`)
            const body_start = ct_pos;
            if (findStatementEnd(sql, name_end + 1)) |body_end| {
                const body = sql[body_start..body_end];
                try tables.append(alloc, .{ .name = name, .body = body });
                i = body_end;
                continue;
            }
        }
        break;
    }

    return try tables.toOwnedSlice(alloc);
}

fn findSubstring(haystack: []const u8, start: usize, needle: []const u8) ?usize {
    if (needle.len == 0) return start;
    if (start + needle.len > haystack.len) return null;
    var i = start;
    while (i <= haystack.len - needle.len) : (i += 1) {
        if (std.mem.eql(u8, haystack[i .. i + needle.len], needle)) return i;
    }
    return null;
}

fn findChar(haystack: []const u8, start: usize, c: u8) ?usize {
    var i = start;
    while (i < haystack.len) : (i += 1) {
        if (haystack[i] == c) return i;
    }
    return null;
}

fn findStatementEnd(sql: []const u8, start: usize) ?usize {
    // Find the semicolon that ends the CREATE TABLE statement
    // Need to handle quoted strings and parentheses
    var depth: usize = 0;
    var in_quote = false;
    var i = start;
    while (i < sql.len) : (i += 1) {
        const c = sql[i];
        if (c == '\'' and (i == 0 or sql[i - 1] != '\\')) {
            in_quote = !in_quote;
        } else if (!in_quote) {
            if (c == '(') {
                depth += 1;
            } else if (c == ')') {
                if (depth == 0) return null; // malformed
                depth -= 1;
            } else if (c == ';' and depth == 0) {
                return i + 1;
            }
        }
    }
    return null;
}

// ─── Diff Printer ──────────────────────────────────────────

pub fn printDiff(old_sql: []const u8, new_sql: []const u8, alloc: std.mem.Allocator) !void {
    const old_tables = try parseCreateTables(alloc, old_sql);
    const new_tables = try parseCreateTables(alloc, new_sql);

    var old_map = std.StringHashMap(usize).init(alloc);
    for (old_tables, 0..) |t, i| try old_map.put(t.name, i);
    var new_map = std.StringHashMap(usize).init(alloc);
    for (new_tables, 0..) |t, i| try new_map.put(t.name, i);

    var has_changes = false;

    for (old_tables) |ot| {
        if (!new_map.contains(ot.name)) {
            std.debug.print("-- DROP TABLE `{s}`\n", .{ot.name});
            has_changes = true;
        }
    }

    for (new_tables) |nt| {
        if (!old_map.contains(nt.name)) {
            std.debug.print("-- CREATE TABLE `{s}`\n", .{nt.name});
            has_changes = true;
        }
    }

    for (old_tables) |ot| {
        if (new_map.get(ot.name)) |ni| {
            const nt = new_tables[ni];
            if (!std.mem.eql(u8, ot.body, nt.body)) {
                std.debug.print("-- ALTER TABLE `{s}`\n", .{ot.name});
                has_changes = true;
            }
        }
    }

    if (!has_changes) {
        std.debug.print("No differences found.\n", .{});
    }
}
