const std = @import("std");
const diff_mod = @import("diff.zig");
const sem = @import("semantic.zig");
const ast_mod = @import("ast.zig");
const codegen = @import("codegen.zig");
const Field = ast_mod.Field;
const TypeInfo = ast_mod.TypeInfo;

pub fn generateFromDiff(
    alloc: std.mem.Allocator,
    d: diff_mod.SchemaDiff,
    old_ast: sem.ResolvedAst,
    new_ast: sem.ResolvedAst,
    dialect: codegen.Dialect,
) ![]const u8 {
    _ = old_ast;
    var aw = std.Io.Writer.Allocating.init(alloc);
    const w = &aw.writer;

    try w.writeAll("-- Migration: schema diff\n");
    try w.writeAll("-- Generated by zig-typespec migrate\n\n");
    try w.writeAll("BEGIN;\n\n");

    // Track if anything was emitted
    var has_operations = false;

    // 1. Dropped tables
    for (d.dropped_tables) |tname| {
        has_operations = true;
        try w.writeAll("DROP TABLE IF EXISTS ");
        try quoteIdent(w, dialect, tname);
        try w.writeAll(";\n\n");
    }

    // 2. Table diffs (create or alter)
    for (d.table_diffs) |td| {
        switch (td.action) {
            .create => {
                // New table — emit CREATE TABLE using the new AST table
                has_operations = true;
                if (findResolvedTable(new_ast, td.name)) |table| {
                    var cg = codegen.Codegen.init(alloc, dialect);
                    const sql = try cg.generateSingleTable(table, new_ast.schema_charset);
                    try w.writeAll(sql);
                    try w.writeAll("\n\n");
                }
            },
            .alter => {
                // Modified table — emit ALTER statements for each diff
                var table_has_ops = false;

                // Field diffs
                for (td.field_diffs) |fd| {
                    switch (fd.action) {
                        .add => {
                            if (!table_has_ops) {
                                try emitAlterTable(w, dialect, td.name);
                                table_has_ops = true;
                            }
                            if (fd.new_field) |nf| {
                                try w.writeAll("ADD COLUMN ");
                                try emitColumnDef(w, dialect, nf);
                                try w.writeAll(",\n");
                            }
                        },
                        .drop => {
                            if (!table_has_ops) {
                                try emitAlterTable(w, dialect, td.name);
                                table_has_ops = true;
                            }
                            try w.writeAll("DROP COLUMN ");
                            try quoteIdent(w, dialect, fd.name);
                            try w.writeAll(",\n");
                        },
                        .modify => {
                            if (!table_has_ops) {
                                try emitAlterTable(w, dialect, td.name);
                                table_has_ops = true;
                            }
                            if (fd.new_field) |nf| {
                                try w.writeAll("MODIFY COLUMN ");
                                try emitColumnDef(w, dialect, nf);
                                try w.writeAll(",\n");
                            }
                        },
                        .rename => {
                            if (!table_has_ops) {
                                try emitAlterTable(w, dialect, td.name);
                                table_has_ops = true;
                            }
                            if (fd.rename_from) |old_name| {
                                switch (dialect) {
                                    .mysql => {
                                        try w.writeAll("CHANGE COLUMN ");
                                        try quoteIdent(w, dialect, old_name);
                                        try w.writeAll(" ");
                                        if (fd.new_field) |nf| {
                                            try emitColumnDef(w, dialect, nf);
                                        }
                                        try w.writeAll(",\n");
                                    },
                                    .postgres => {
                                        try w.writeAll("RENAME COLUMN ");
                                        try quoteIdent(w, dialect, old_name);
                                        try w.writeAll(" TO ");
                                        try quoteIdent(w, dialect, fd.name);
                                        try w.writeAll(",\n");
                                    },
                                }
                            }
                        },
                    }
                }

                // Index diffs
                for (td.index_diffs) |idx_diff| {
                    switch (idx_diff.action) {
                        .add => {
                            if (idx_diff.new_idx) |idx| {
                                if (!table_has_ops) {
                                    try emitAlterTable(w, dialect, td.name);
                                    table_has_ops = true;
                                }
                                try emitAddIndex(w, dialect, td.name, idx);
                                try w.writeAll(",\n");
                            }
                        },
                        .drop => {
                            if (idx_diff.old_idx) |idx| {
                                if (!table_has_ops) {
                                    try emitAlterTable(w, dialect, td.name);
                                    table_has_ops = true;
                                }
                                try emitDropIndex(w, dialect, td.name, idx);
                                try w.writeAll(",\n");
                            }
                        },
                        .modify => {
                            // Drop old + add new
                            if (idx_diff.old_idx) |old_idx| {
                                if (!table_has_ops) {
                                    try emitAlterTable(w, dialect, td.name);
                                    table_has_ops = true;
                                }
                                try emitDropIndex(w, dialect, td.name, old_idx);
                                try w.writeAll(",\n");
                            }
                            if (idx_diff.new_idx) |new_idx| {
                                if (!table_has_ops) {
                                    try emitAlterTable(w, dialect, td.name);
                                    table_has_ops = true;
                                }
                                try emitAddIndex(w, dialect, td.name, new_idx);
                                try w.writeAll(",\n");
                            }
                        },
                    }
                }

                // FK diffs
                for (td.fk_diffs) |fk_diff| {
                    switch (fk_diff.action) {
                        .add => {
                            if (fk_diff.new_fk) |fk| {
                                if (!table_has_ops) {
                                    try emitAlterTable(w, dialect, td.name);
                                    table_has_ops = true;
                                }
                                try emitAddFk(w, dialect, td.name, fk);
                                try w.writeAll(",\n");
                            }
                        },
                        .drop => {
                            if (fk_diff.old_fk) |fk| {
                                if (!table_has_ops) {
                                    try emitAlterTable(w, dialect, td.name);
                                    table_has_ops = true;
                                }
                                try emitDropFk(w, dialect, td.name, fk);
                                try w.writeAll(",\n");
                            }
                        },
                    }
                }

                if (table_has_ops) {
                    has_operations = true;
                    // Remove trailing ",\n" and replace with ";\n"
                    // We already wrote trailing comma, so just close
                    try w.writeAll("-- end\n");
                    // We need to rewrite — use a simpler approach: just close with semicolon
                    // The trailing comma approach won't work cleanly, so let's use a different strategy
                }
            },
        }
    }

    try w.writeAll("COMMIT;\n");

    try w.flush();
    var out = aw.toArrayList();
    const raw_result = try out.toOwnedSlice(alloc);

    // Post-process: replace ",\n-- end\n" with ";\n"
    return try fixTrailingCommas(alloc, raw_result);
}

/// Replace trailing ",\n" before COMMIT or before "-- end" markers with ";\n"
fn fixTrailingCommas(alloc: std.mem.Allocator, sql: []const u8) ![]const u8 {
    var result = try std.ArrayList(u8).initCapacity(alloc, sql.len);
    var i: usize = 0;
    while (i < sql.len) {
        // Look for ",\n-- end\n"
        if (i + 8 <= sql.len and std.mem.eql(u8, sql[i .. i + 8], ",\n-- end")) {
            try result.appendSlice(alloc, ";\n");
            i += 8;
            // Skip the trailing \n after -- end
            if (i < sql.len and sql[i] == '\n') i += 1;
            continue;
        }
        try result.append(alloc, sql[i]);
        i += 1;
    }
    return try result.toOwnedSlice(alloc);
}

fn emitAlterTable(w: anytype, dialect: codegen.Dialect, table_name: []const u8) !void {
    try w.writeAll("ALTER TABLE ");
    try quoteIdent(w, dialect, table_name);
    try w.writeAll("\n");
}

fn emitColumnDef(w: anytype, dialect: codegen.Dialect, field: Field) !void {
    try quoteIdent(w, dialect, field.name);
    try w.writeAll(" ");
    try emitTypeForColumn(w, dialect, field.type_info);

    // Modifiers
    for (field.modifiers) |mod| {
        switch (mod.kind) {
            .auto_inc_pk => {
                switch (dialect) {
                    .mysql => try w.writeAll(" AUTO_INCREMENT PRIMARY KEY"),
                    .postgres => try w.writeAll(" GENERATED ALWAYS AS IDENTITY PRIMARY KEY"),
                }
            },
            .auto_inc => {
                switch (dialect) {
                    .mysql => try w.writeAll(" AUTO_INCREMENT"),
                    .postgres => try w.writeAll(" GENERATED ALWAYS AS IDENTITY"),
                }
            },
            .primary_key => try w.writeAll(" PRIMARY KEY"),
            .not_null => try w.writeAll(" NOT NULL"),
            .unsigned => {
                switch (dialect) {
                    .mysql => try w.writeAll(" UNSIGNED"),
                    .postgres => {},
                }
            },
            .inline_unique => {},
            .inline_index => {},
        }
    }

    // Default value
    if (field.default_val) |dv| {
        const is_num_val = blk: {
            _ = std.fmt.parseFloat(f64, dv.value) catch break :blk false;
            break :blk true;
        };
        const is_sql_keyword = std.mem.eql(u8, dv.value, "CURRENT_TIMESTAMP") or
            std.mem.eql(u8, dv.value, "NULL") or
            std.mem.eql(u8, dv.value, "NOW()");
        if (is_num_val or is_sql_keyword) {
            try w.print(" DEFAULT {s}", .{dv.value});
        } else {
            try w.print(" DEFAULT '{s}'", .{dv.value});
        }
    }

    // CHECK constraint
    if (field.check) |ck| {
        try w.writeAll(" CHECK (");
        try codegen.generateCheckExpr(w, field.name, ck);
        try w.writeAll(")");
    }
}

fn emitTypeForColumn(w: anytype, dialect: codegen.Dialect, type_info: TypeInfo) !void {
    switch (dialect) {
        .mysql => {
            switch (type_info) {
                .none => try w.writeAll("varchar(255)"),
                .simple => |s| {
                    if (s.len == 1) {
                        switch (s[0]) {
                            'n' => try w.writeAll("int"),
                            'N' => try w.writeAll("bigint"),
                            'm' => try w.writeAll("decimal(16, 2)"),
                            'M' => try w.writeAll("decimal(20, 6)"),
                            'S' => try w.writeAll("text"),
                            'b' => try w.writeAll("boolean"),
                            'B' => try w.writeAll("blob"),
                            'j' => try w.writeAll("json"),
                            'd' => try w.writeAll("date"),
                            't' => try w.writeAll("datetime"),
                            else => try w.writeAll(s),
                        }
                    } else {
                        try w.writeAll(s);
                    }
                },
                .int_explicit => |n| try w.print("int({d})", .{n}),
                .decimal_explicit => |ds| try w.print("decimal({d}, {d})", .{ ds.precision, ds.scale }),
                .varchar_explicit => |n| {
                    if (n > 0) try w.print("varchar({d})", .{n}) else try w.writeAll("varchar(255)");
                },
                .enum_type => |vals| {
                    try w.writeAll("ENUM(");
                    for (vals, 0..) |v, vi| {
                        if (vi > 0) try w.writeAll(", ");
                        try w.print("'{s}'", .{v});
                    }
                    try w.writeAll(")");
                },
            }
        },
        .postgres => {
            switch (type_info) {
                .none => try w.writeAll("varchar(255)"),
                .simple => |s| {
                    if (s.len == 1) {
                        switch (s[0]) {
                            'n' => try w.writeAll("integer"),
                            'N' => try w.writeAll("bigint"),
                            'm' => try w.writeAll("numeric(16, 2)"),
                            'M' => try w.writeAll("numeric(20, 6)"),
                            'S' => try w.writeAll("text"),
                            'b' => try w.writeAll("boolean"),
                            'B' => try w.writeAll("bytea"),
                            'j' => try w.writeAll("json"),
                            'd' => try w.writeAll("date"),
                            't' => try w.writeAll("timestamp"),
                            else => try w.writeAll(s),
                        }
                    } else {
                        try w.writeAll(s);
                    }
                },
                .int_explicit => |n| {
                    _ = n;
                    try w.writeAll("integer");
                },
                .decimal_explicit => |ds| try w.print("numeric({d}, {d})", .{ ds.precision, ds.scale }),
                .varchar_explicit => |n| {
                    if (n > 0) try w.print("varchar({d})", .{n}) else try w.writeAll("varchar(255)");
                },
                .enum_type => |vals| {
                    try w.writeAll("text");
                    _ = vals;
                },
            }
        },
    }
}

fn emitAddIndex(w: anytype, dialect: codegen.Dialect, _: []const u8, idx: ast_mod.IndexDecl) !void {
    switch (dialect) {
        .mysql => {
            switch (idx.kind) {
                .regular => {
                    try w.print("ADD INDEX `{s}` (", .{idx.name});
                    try emitIndexFields(w, .mysql, idx);
                    try w.writeAll(")");
                },
                .unique => {
                    try w.print("ADD UNIQUE INDEX `{s}` (", .{idx.name});
                    try emitIndexFields(w, .mysql, idx);
                    try w.writeAll(")");
                },
                .fulltext => {
                    try w.print("ADD FULLTEXT INDEX `{s}` (", .{idx.name});
                    try emitIndexFields(w, .mysql, idx);
                    try w.writeAll(")");
                },
                .primary_key => {
                    try w.writeAll("ADD PRIMARY KEY (");
                    try emitIndexFields(w, .mysql, idx);
                    try w.writeAll(")");
                },
            }
        },
        .postgres => {
            switch (idx.kind) {
                .unique => {
                    try w.writeAll("ADD UNIQUE (");
                    try emitIndexFields(w, .postgres, idx);
                    try w.writeAll(")");
                },
                .primary_key => {
                    try w.writeAll("ADD PRIMARY KEY (");
                    try emitIndexFields(w, .postgres, idx);
                    try w.writeAll(")");
                },
                else => {
                    // PG: regular/fulltext indexes can't be added via ALTER TABLE
                    // Emit as comment
                    try w.print("-- NOTE: CREATE INDEX needed for '{s}' (not supported via ALTER TABLE in PG)\n", .{idx.name});
                },
            }
        },
    }
}

fn emitDropIndex(w: anytype, dialect: codegen.Dialect, table_name: []const u8, idx: ast_mod.IndexDecl) !void {
    _ = table_name;
    switch (dialect) {
        .mysql => {
            switch (idx.kind) {
                .primary_key => try w.writeAll("DROP PRIMARY KEY"),
                else => try w.print("DROP INDEX `{s}`", .{idx.name}),
            }
        },
        .postgres => {
            switch (idx.kind) {
                .primary_key => try w.writeAll("DROP PRIMARY KEY"),
                else => try w.print("DROP INDEX \"{s}\"", .{idx.name}),
            }
        },
    }
}

fn emitIndexFields(w: anytype, dialect: codegen.Dialect, idx: ast_mod.IndexDecl) !void {
    for (idx.fields, 0..) |f, fi| {
        if (fi > 0) try w.writeAll(", ");
        switch (dialect) {
            .mysql => try w.print("`{s}`", .{f}),
            .postgres => try w.print("\"{s}\"", .{f}),
        }
    }
}

fn emitAddFk(w: anytype, dialect: codegen.Dialect, table_name: []const u8, fk: ast_mod.FkDecl) !void {
    _ = table_name;
    try w.writeAll("ADD FOREIGN KEY (");
    for (fk.fields, 0..) |f, fi| {
        if (fi > 0) try w.writeAll(", ");
        try quoteIdent(w, dialect, f);
    }
    try w.writeAll(") REFERENCES ");
    try quoteIdent(w, dialect, fk.ref_table);
    try w.writeAll("(");
    for (fk.ref_fields, 0..) |f, fi| {
        if (fi > 0) try w.writeAll(", ");
        try quoteIdent(w, dialect, f);
    }
    try w.writeAll(")");
    for (fk.actions) |action| {
        try w.writeAll(" ");
        switch (action.trigger) {
            .on_delete => try w.writeAll("ON DELETE"),
            .on_update => try w.writeAll("ON UPDATE"),
        }
        try w.writeAll(" ");
        switch (action.action) {
            .cascade => try w.writeAll("CASCADE"),
            .set_null => try w.writeAll("SET NULL"),
        }
    }
}

fn emitDropFk(w: anytype, dialect: codegen.Dialect, table_name: []const u8, fk: ast_mod.FkDecl) !void {
    _ = table_name;
    // Generate a FK constraint name from the local fields
    try w.writeAll("DROP FOREIGN KEY ");
    switch (dialect) {
        .mysql => {
            try w.writeAll("fk_");
            for (fk.fields) |f| {
                try w.writeAll(f);
            }
        },
        .postgres => {
            try w.writeAll("\"fk_");
            for (fk.fields) |f| {
                try w.writeAll(f);
            }
            try w.writeAll("\"");
        },
    }
}

fn quoteIdent(w: anytype, dialect: codegen.Dialect, name: []const u8) !void {
    switch (dialect) {
        .mysql => try w.print("`{s}`", .{name}),
        .postgres => try w.print("\"{s}\"", .{name}),
    }
}

fn findResolvedTable(ast: sem.ResolvedAst, name: []const u8) ?sem.ResolvedTable {
    for (ast.tables) |table| {
        if (std.mem.eql(u8, table.name, name)) return table;
    }
    return null;
}
